#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
const ll mx = 501;
bool adjMat[mx][mx];
bool adj[mx][mx];
map<string, ll>  mp;

int main(){
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
    ll n, m;
    cin >> n >> m;
    ll index = -1;
    for (ll i = 0; i < n; ++i) {
        string a, b, c;
        cin >> a >> b >> c;
        if (mp.count(a) == 0) {
            index++;
            mp[a] = index;
        }
        if (mp.count(c) == 0) {
            index++;
            mp[c] = index;
        }

        if (b == "is-a") {
            adjMat[mp[a]][mp[c]] = true;
        }
        else {
            adj[mp[a]][mp[c]] = true;
        }
    }
    for(ll i=0;i<mp.size();i++){
    	adjMat[i][i]=true;
    }
    for (ll k = 0; k < mx; ++k) {
        for (ll i = 0; i < mx; ++i) {
            for (ll j = 0; j < mx; ++j) {
                adjMat[i][j] = adjMat[i][j] || (adjMat[i][k] && adjMat[k][j]);
                adj[i][j] = adj[i][j] || (adj[i][k] && adj[k][j] );
                adj[i][j] = adj[i][j] || (adjMat[i][k] && adj[k][j]);
                adj[i][j] = adj[i][j] || (adj[i][k] && adjMat[k][j]);
            }
        }
    }

    ll q = 1;
    for (ll i = 0; i < m; ++i) {
    	bool ok;
        string a, b, c;
        cin >> a >> b >> c;
        if (b=="is-a") {
            ok = adjMat[mp[a]][mp[c]];
        }
        else {
            ok = adj[mp[a]][mp[c]];
        }
        if(ok)
            cout << "Query " << q << ": true" << endl;
        else
            cout << "Query " << q << ": false" << endl;
        q++;
    }

    return 0;
}