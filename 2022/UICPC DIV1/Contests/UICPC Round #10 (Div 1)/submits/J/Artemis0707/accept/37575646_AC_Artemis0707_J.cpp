#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main(){
	ll t;
	cin >> t;
	while(t--){
		ll n,m;
		string ans = "";
		cin >> n >> m;
		ll r_max = INT_MIN,w_max = INT_MIN;
		for(ll i=0;i<m;i++){
			ll r,w;
			cin >> r >> w;
			r_max = max(r_max,r);
			w_max = max(w_max,w);
		}
		if(r_max+w_max<=n){
			for(ll i=0;i<r_max;i++)ans+='R';
			for(ll i=0;i<n-r_max;i++)ans+='W';
		}
		else{
			ans = "IMPOSSIBLE";
		}
		cout<<ans<<endl;
	}
	return 0;
}