#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
ll bs[22][360];
bool visited[22][360];
ll n;
bool ok = false;
pair<ll,ll> mov[4] = {{0,1},{1,0},{-1,0},{0,-1}};
bool inr(ll r){
	return r>=0 && r<22;
}
void dfs (ll r, ll d){
	if(ok) return;
	visited[r][d] = true;
	if(r>20){
		ok = true;
		return;
	}
	for(auto m:mov){
		ll vx = r + m.first;
		ll vy = (r + m.second)%360;
		if(inr(vx) && visited[vx][vy]==false){
			dfs(vx,vy);
			if(ok)return;
		}
	}

}
int main(){
	ll t;
	cin >> t;
	while(t--){
		ok = false;
		memset(bs,0,sizeof(bs));
		cin >> n;
		for(ll i=0;i<n;i++){
			char w;
			cin >> w;
			if(w=='C'){
				ll r,x,y; cin >> r >> x >> y;
				for(ll j=x;j!=y;j = (j+1)%360){
					bs[r][j] = 1;
				}
			}
			else{
				ll x,r1,r2; cin >> x >> r1 >> r2;
				for(ll j=r1;j<r2;j++){
					bs[x][j] = 1;
				}
			}
		}
		dfs(0,0);
		cout<<(ok ? "YES":"NO")<<endl;

	}	
	return 0;
}