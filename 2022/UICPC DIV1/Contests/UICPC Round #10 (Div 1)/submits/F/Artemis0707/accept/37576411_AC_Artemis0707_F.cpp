#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
set<ll> ans;
ll w,l;
ll gcd(ll a, ll b){
	return b == 0 ? a:gcd(b,a%b);
}
void count(ll r){
	for(ll i=1;i*i<=r;i++){
		if(r%i==0){
			ans.insert(i);
			ans.insert(r/i);
		}
	}
}
int main(){
	ll t;
	cin >> t;
	while(t--){
		ans.clear();
		cin >> w >> l;
		count(gcd(w-2,l));
		count(gcd(w-1,l-1));
		count(gcd(l-2,w));
		count(gcd(gcd(w-1,l-2),l));
		count(gcd(gcd(w-2,w),l-1));
		cout<<ans.size()<<' ';
		for(auto num:ans){
			cout<<num<<' ';
		}
		cout<<endl;

	}
	return 0;
}